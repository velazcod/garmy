"""
Fuzzy exercise name matching for Garmin workouts.

This module provides intelligent matching from user-friendly exercise names
(e.g., "bench press", "dumbbell curl") to Garmin's required SCREAMING_SNAKE_CASE
format (e.g., BARBELL_BENCH_PRESS, DUMBBELL_BICEPS_CURL).

Example:
    >>> from garmy.workouts.exercises import ExerciseMatcher
    >>> matcher = ExerciseMatcher()
    >>> result = matcher.resolve("bench press")
    >>> result.name
    'BARBELL_BENCH_PRESS'
    >>> result.category
    'BENCH_PRESS'
"""

from dataclasses import dataclass, field
from difflib import SequenceMatcher
from typing import Dict, List, Optional, Set, Tuple
import re


@dataclass(frozen=True)
class MatchResult:
    """Result of fuzzy exercise name matching.

    Attributes:
        name: Garmin exercise name in SCREAMING_SNAKE_CASE
        category: Garmin exercise category
        score: Match confidence score (0.0 to 1.0)
        alternatives: Other potential matches with their scores
    """

    name: str
    category: str
    score: float
    alternatives: List[Tuple[str, str, float]] = field(default_factory=list)

    @property
    def is_exact(self) -> bool:
        """True if this was an exact match."""
        return self.score >= 1.0

    @property
    def is_confident(self) -> bool:
        """True if confidence is high enough to use without warning."""
        return self.score >= 0.8

    def __str__(self) -> str:
        return f"{self.name} ({self.score:.0%})"


