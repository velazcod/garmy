class ExerciseMatcher:
    """Fuzzy matcher for Garmin exercise names.

    Converts user-friendly exercise names to Garmin's SCREAMING_SNAKE_CASE format
    using a hybrid approach of alias lookup, token matching, and Levenshtein distance.

    Example:
        >>> matcher = ExerciseMatcher()
        >>> result = matcher.resolve("dumbbell bench press")
        >>> result.name
        'DUMBBELL_BENCH_PRESS'

        >>> result = matcher.resolve("dumbell curl")  # typo handled
        >>> result.name
        'DUMBBELL_BICEPS_CURL'

        >>> results = matcher.search("squat", limit=5)
        >>> [r.name for r in results]
        ['BARBELL_SQUAT', 'GOBLET_SQUAT', 'FRONT_SQUAT', ...]
    """

    def __init__(self, threshold: float = 0.5) -> None:
        """Initialize the exercise matcher.

        Args:
            threshold: Minimum confidence score (0-1) to return a match.
                      Default 0.5 balances precision and recall.
        """
        self._threshold = threshold
        self._exercises = EXERCISES
        self._aliases = ALIASES
        self._exercise_names = list(EXERCISES.keys())
        self._normalized_index: Dict[str, str] = {}
        self._build_index()

    def _build_index(self) -> None:
        """Build normalized index for fast lookup."""
        for name in self._exercise_names:
            normalized = self._normalize(name)
            self._normalized_index[normalized] = name

    @staticmethod
    def _normalize(text: str) -> str:
        """Normalize text for matching.

        Converts to lowercase, removes punctuation, replaces spaces with underscores.
        """
        text = text.lower().strip()
        text = re.sub(r"[^\w\s]", "", text)
        text = re.sub(r"\s+", "_", text)
        return text

    @staticmethod
    def _tokenize(text: str) -> Set[str]:
        """Extract tokens from text."""
        normalized = ExerciseMatcher._normalize(text)
        return set(normalized.split("_"))

    @staticmethod
    def _levenshtein_ratio(s1: str, s2: str) -> float:
        """Calculate similarity ratio using SequenceMatcher."""
        return SequenceMatcher(None, s1, s2).ratio()

    def _score_candidate(
        self, query_tokens: Set[str], candidate: str, query_normalized: str
    ) -> float:
        """Score a candidate exercise against the query.

        Uses a combination of:
        1. Exact normalized match
        2. Token overlap (Jaccard similarity)
        3. Fuzzy token matching (Levenshtein)
        4. Overall string similarity
        """
        candidate_normalized = self._normalize(candidate)
        candidate_tokens = self._tokenize(candidate)

        # Exact match after normalization
        if query_normalized == candidate_normalized:
            return 1.0

        # Token overlap score (Jaccard)
        intersection = query_tokens & candidate_tokens
        union = query_tokens | candidate_tokens
        token_score = len(intersection) / len(union) if union else 0

        # Fuzzy token matching - find best match for each query token
        fuzzy_scores = []
        for qt in query_tokens:
            if qt in candidate_tokens:
                fuzzy_scores.append(1.0)
            else:
                best = max(
                    (self._levenshtein_ratio(qt, ct) for ct in candidate_tokens),
                    default=0,
                )
                fuzzy_scores.append(best)
        fuzzy_score = sum(fuzzy_scores) / len(fuzzy_scores) if fuzzy_scores else 0

        # Overall string similarity
        string_score = self._levenshtein_ratio(query_normalized, candidate_normalized)

        # Weighted combination
        # Token overlap is most important, then fuzzy tokens, then overall string
        return (token_score * 0.4) + (fuzzy_score * 0.4) + (string_score * 0.2)

    def resolve(
        self, query: str, equipment_hint: Optional[str] = None
    ) -> Optional[MatchResult]:
        """Find the best matching Garmin exercise name.

        Args:
            query: User-provided exercise name (any format)
            equipment_hint: Optional equipment to prefer (e.g., "dumbbell")

        Returns:
            MatchResult if confidence >= threshold, else None
        """
        if not query or not query.strip():
            return None

        query_normalized = self._normalize(query)
        query_tokens = self._tokenize(query)

        # 1. Check exact match in index
        if query_normalized in self._normalized_index:
            name = self._normalized_index[query_normalized]
            return MatchResult(
                name=name,
                category=self._exercises[name],
                score=1.0,
            )

        # 2. Check aliases
        query_lower = query.lower().strip()
        if query_lower in self._aliases:
            name = self._aliases[query_lower]
            if name in self._exercises:
                return MatchResult(
                    name=name,
                    category=self._exercises[name],
                    score=0.95,  # High confidence for alias match
                )

        # 3. Score all candidates
        candidates: List[Tuple[str, float]] = []
        for exercise_name in self._exercise_names:
            score = self._score_candidate(query_tokens, exercise_name, query_normalized)

            # Apply equipment hint bonus
            if equipment_hint:
                hint_normalized = self._normalize(equipment_hint)
                if hint_normalized.upper() in exercise_name:
                    score = min(1.0, score + 0.15)

            candidates.append((exercise_name, score))

        # Sort by score descending
        candidates.sort(key=lambda x: x[1], reverse=True)

        if not candidates or candidates[0][1] < self._threshold:
            return None

        best_name, best_score = candidates[0]

        # Get alternatives (top 5 excluding best)
        alternatives = [
            (name, self._exercises[name], score)
            for name, score in candidates[1:6]
            if score >= self._threshold
        ]

        return MatchResult(
            name=best_name,
            category=self._exercises[best_name],
            score=best_score,
            alternatives=alternatives,
        )

    def resolve_or_raise(
        self, query: str, equipment_hint: Optional[str] = None
    ) -> MatchResult:
        """Resolve exercise name or raise ValueError with suggestions.

        Args:
            query: User-provided exercise name
            equipment_hint: Optional equipment preference

        Returns:
            MatchResult for the best match

        Raises:
            ValueError: If no match found, includes suggestions
        """
        result = self.resolve(query, equipment_hint)
        if result is None:
            suggestions = self.search(query, limit=3)
            if suggestions:
                suggestion_str = ", ".join(r.name for r in suggestions)
                raise ValueError(
                    f"No match found for '{query}'. Did you mean: {suggestion_str}?"
                )
            else:
                raise ValueError(
                    f"No match found for '{query}'. "
                    f"Use search_exercises to find valid exercise names."
                )
        return result

    def search(self, query: str, limit: int = 10) -> List[MatchResult]:
        """Search for exercises matching the query.

        Args:
            query: Search term
            limit: Maximum number of results

        Returns:
            List of MatchResult sorted by score descending
        """
        if not query or not query.strip():
            return []

        query_normalized = self._normalize(query)
        query_tokens = self._tokenize(query)

        # Score all candidates
        candidates: List[Tuple[str, float]] = []
        for exercise_name in self._exercise_names:
            score = self._score_candidate(query_tokens, exercise_name, query_normalized)
            candidates.append((exercise_name, score))

        # Sort by score and take top N
        candidates.sort(key=lambda x: x[1], reverse=True)
        top_candidates = candidates[:limit]

        return [
            MatchResult(
                name=name,
                category=self._exercises[name],
                score=score,
            )
            for name, score in top_candidates
            if score > 0
        ]

    def get_category(self, exercise_name: str) -> Optional[str]:
        """Get the category for a Garmin exercise name.

        Args:
            exercise_name: Exercise name in any format

        Returns:
            Category name or None if not found
        """
        # Try exact match first
        if exercise_name in self._exercises:
            return self._exercises[exercise_name]

        # Try normalized match
        normalized = self._normalize(exercise_name)
        if normalized in self._normalized_index:
            name = self._normalized_index[normalized]
            return self._exercises[name]

        return None

    def list_categories(self) -> List[str]:
        """List all unique exercise categories."""
        return sorted(set(self._exercises.values()))

    def list_by_category(self, category: str) -> List[str]:
        """List all exercises in a category.

        Args:
            category: Category name (case-insensitive)

        Returns:
            List of exercise names in the category
        """
        category_upper = category.upper()
        return sorted(
            name for name, cat in self._exercises.items() if cat == category_upper
        )

    def list_by_equipment(self, equipment: str) -> List[str]:
        """List all exercises for an equipment type.

        Args:
            equipment: Equipment prefix (e.g., "DUMBBELL", "BARBELL")

        Returns:
            List of exercise names using that equipment
        """
        equipment_upper = equipment.upper()
        return sorted(
            name for name in self._exercise_names if name.startswith(equipment_upper)
        )


