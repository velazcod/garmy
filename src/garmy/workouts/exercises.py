_default_matcher: Optional[ExerciseMatcher] = None


def get_matcher() -> ExerciseMatcher:
    """Get or create the default ExerciseMatcher instance."""
    global _default_matcher
    if _default_matcher is None:
        _default_matcher = ExerciseMatcher()
    return _default_matcher


def resolve_exercise(
    query: str, equipment_hint: Optional[str] = None
) -> Tuple[str, str]:
    """Resolve a user-friendly exercise name to Garmin format.

    Convenience function that returns (exercise_name, category) tuple.

    Args:
        query: User-provided exercise name
        equipment_hint: Optional equipment preference

    Returns:
        Tuple of (garmin_name, category)

    Raises:
        ValueError: If no match found
    """
    result = get_matcher().resolve_or_raise(query, equipment_hint)
    return result.name, result.category


def search_exercises(query: str, limit: int = 10) -> List[MatchResult]:
    """Search for exercises matching the query.

    Convenience function for exercise discovery.

    Args:
        query: Search term
        limit: Maximum results

    Returns:
        List of MatchResult
    """
    return get_matcher().search(query, limit)
